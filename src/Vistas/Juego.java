    private void cmbLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbLoginActionPerformed
        switch (cmbLogin.getSelectedIndex()) {
            case 1:
                if (!txtNick.getText().trim().equalsIgnoreCase("") || !txtPass.getText().trim().equalsIgnoreCase("")) {
                    jugador = new Jugador(txtNick.getText(), txtPass.getText());
                    if (jugador.verificarRegistro(txtNick.getText())) {
                        jugador.registrarJugador();
                        JOptionPane.showMessageDialog(panelLogin, "Se ha registrado con exito");
                    } else {
                        JOptionPane.showMessageDialog(panelLogin, "Ya se encuentra registrado en el juego", "Ha ocurrido un problema", JOptionPane.ERROR_MESSAGE);
                    }
                    txtNick.setText("");
                    txtPass.setText("");
                } else {
                    JOptionPane.showMessageDialog(panelLogin, "No puede dejar los espacios en blanco", "Advertencia", JOptionPane.WARNING_MESSAGE);
                    txtNick.setText("");
                    txtPass.setText("");
                }
                break;
            case 2:
                if (!txtNick.getText().trim().equalsIgnoreCase("") || !txtPass.getText().trim().equalsIgnoreCase("")) {
                    jugador = new Jugador();
                    if (jugador.verificarLogin(txtNick.getText(), txtPass.getText())) {
                        cmbOpciones.setVisible(true);
                        labelLogin.setVisible(true);
                        cmbOpciones.setEnabled(true);
                        labelLogin.setEnabled(true);
                        JOptionPane.showMessageDialog(panelLogin, "Ha iniciado sesión correctamente", "Felicidades", JOptionPane.INFORMATION_MESSAGE);
                        txtNick.setText("");
                        txtPass.setText("");
                    }
                } else {
                    JOptionPane.showMessageDialog(panelLogin, "No puede dejar los espacios en blanco", "Advertencia", JOptionPane.WARNING_MESSAGE);
                    txtNick.setText("");
                    txtPass.setText("");
                }
                break;
        }
    }//GEN-LAST:event_cmbLoginActionPerformed

